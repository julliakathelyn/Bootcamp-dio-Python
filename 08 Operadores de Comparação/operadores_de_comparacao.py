#Sempre vai volltar um valor booleano, true se a comparação for verdadeira e false caso não seja

saldo = 450
saque = 200

# igualdade
print(saldo == saque) 

# diferença
print(saldo != saque)

#Maior que 
print(saldo > saque)

#Maior ou igual
print(saldo >= saque)

#Menor que
print(saldo < saque)

#Menor ou igual
print(saldo <= saque)